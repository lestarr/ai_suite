# Function to clean Price values
def clean_Price(Price):
    if isinstance(Price, (int, float)):
        return Price
    if Price == "Included" or Price == "0.00" or Price == "0.32":
        return 0.0
    # Remove '$' and convert to float
    return float(Price.replace('$', ''))

# Function to clean boolean values
def clean_Office(Office):
    if isinstance(Office, bool):
        return Office
    return Office.lower() == "true"

